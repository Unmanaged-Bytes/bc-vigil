def current_lang(request: Request) -> str:
    lang = request.cookies.get(COOKIE_NAME, DEFAULT_LANG)
    return lang if lang in SUPPORTED else DEFAULT_LANG


def translate(request: Request, key: str) -> str:
    lang = current_lang(request)
    return (
        TRANSLATIONS.get(lang, {}).get(key)
        or TRANSLATIONS[DEFAULT_LANG].get(key)
        or key
    )
